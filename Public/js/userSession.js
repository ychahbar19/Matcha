function getLocation(position){
  var latitude = position.coords.latitude;
  var longitude = position.coords.longitude;
  var token_access = "f79132de53b3f1";
  var city;
  var country;

  $.post("https://eu1.locationiq.com/v1/reverse.php",
        {
          key: token_access,
          lat: latitude,
          lon: longitude,
          format: 'json'
        },
        function(data){
          city = data.address.city_district;
          country = data.address.country;
          $.post("http://localhost:8888/matcha/index.php?request=userSession.locate.set",
          {latitude: position.coords.latitude,
           longitude: position.coords.longitude,
           country: country
         }
          );
      });
}

function currentLocation(){
  if (navigator.geolocation) {
    navigator.geolocation.getCurrentPosition(getLocation, function(error){
      if (error.code === error.PERMISSION_DENIED)
      {
        $.post("http://localhost:8888/matcha/index.php?request=userSession.locate.set",
        {permission: false}
        );
      }
    });
  } else {
    alert("Geolocation is not supported by this browser.");
  }
  return 1;
}

